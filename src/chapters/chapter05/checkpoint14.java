package chapters.chapter05;

public class checkpoint14 {

	public static void main(String[] args) {
		//Multiplication.
		int counter = 0;
		for(int i = 0; i < 3; i++) {
			for(int j = 0; j < 3; j++) {
				for(int k = 0; k < 3; k++) {
					counter++;
					System.out.println("counter: " + counter + " -i:" +i + " j: " +j + " k:" +k);
				}
					
			}
		}

	}

}
